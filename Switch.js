let num = 1;
switch (num) {
  case 1:
    console.log("one");
    break;
  case 1:
    console.log("Two");
    break;
  case 1:
    console.log("Three");
    break;
  default:
    console.log("invalid input");
    break;
}

let letter = "a";
switch (letter) {
  case "a":
  case "e":
  case "i":
  case "o":
  case "U":
  case "A":
  case "E":
  case "I":
  case "0":
    console.log(letter + " is an vowel");
    break;
  default:
    console.log(letter + " is not a vowel");
    break;
}
